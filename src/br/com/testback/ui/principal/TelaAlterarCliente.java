    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
        int resposta = JOptionPane.showConfirmDialog(rootPane,
                "Excluir o cliente \"" + cliente.getNm_customer() + "\"?",
                "Confirmar exclus√£o", JOptionPane.YES_NO_OPTION);        
        if (resposta == JOptionPane.YES_OPTION) {
            ServicoCliente.excluirCliente(cliente.getId_customer());
        }
        
        this.dispose();
    }//GEN-LAST:event_btn_excluirActionPerformed

    private void btn_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_alterarActionPerformed
        cliente.setCpf_cnpj(jtf_cpf_cnpj.getText());
        cliente.setNm_customer(jtf_nome.getText());
        cliente.setVl_total(Double.parseDouble(jtf_saldo.getText()));
        
        ServicoCliente.atualizarCliente(cliente);
        
        this.dispose();
    }//GEN-LAST:event_btn_alterarActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        jtf_cpf_cnpj.setText(cliente.getCpf_cnpj());
        jtf_nome.setText(cliente.getNm_customer());
        jtf_saldo.setText(cliente.getVl_total().toString());
    }//GEN-LAST:event_formWindowOpened

